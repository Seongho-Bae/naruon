def get_fernet() -> Fernet:
    if settings.ENCRYPTION_KEY is not None:
        key = settings.ENCRYPTION_KEY.get_secret_value().encode("utf-8")
        try:
            return Fernet(key)
        except ValueError as exc:
            raise RuntimeError(
                "ENCRYPTION_KEY must be a valid Fernet key generated by "
                "Fernet.generate_key()."
            ) from exc
    raise RuntimeError(
        "ENCRYPTION_KEY is required. Refusing to encrypt without a configured key."
    )


class EncryptedString(TypeDecorator):
    """
    Encrypts string values before saving to the database and decrypts them when
    retrieving.
    Uses Fernet symmetric encryption.
    """

    impl = String
    cache_ok = True

    def process_bind_param(self, value, dialect):
        if value is not None:
            f = get_fernet()
            return f.encrypt(value.encode("utf-8")).decode("utf-8")
        return value

    def process_result_value(self, value, dialect):
        if value is not None:
            f = get_fernet()
            try:
                return f.decrypt(value.encode("utf-8")).decode("utf-8")
            except (InvalidToken, UnicodeDecodeError):
                logger.warning(
                    "Failed to decrypt field in EncryptedString; returning None"
                )
                return None
        return value


